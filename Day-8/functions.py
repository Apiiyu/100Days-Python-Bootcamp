# Functions with Inputs

def say_hello(): # function definition without input
  print("Hello")
  print("How are you?")
  print("How have you been?")
  
say_hello()

def grettings(name): # function definition with input
  print(f"Hello {name}")
  print(f"How are you {name}?")
  print(f"How have you been {name}?")
  
grettings("John")

