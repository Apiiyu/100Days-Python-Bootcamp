# Number Manipulation

# 1. Round decimal number
print(round(8 / 3)) # 3
print(round(8 / 3, 2)) # 2.67

# 2. Floor division
print(8 // 3) # 2

# 3. Shorthand
score = 0
score += 1
print(score) # 1

# 4. F-String
score = 0
height = 1.8
isWinning = True
print(f"Your score is {score}, your height is {height}, you are winning is {isWinning}") # Your score is 0, your height is 1.8, you are winning is True