# Mathematical Operations

# Addition
print(2 + 3)

# Subtraction
print(3 - 2)

# Multiplication
print(2 * 3)

# Division
print(int(6 / 3)) # We should convert the result to int, because the result is float

# Exponent
print(2 ** 3)

""" Order of Operations
  PEMDAS
  1. Parentheses ()
  2. Exponents **
  3. Multiplication *
  4. Division /
  5. Addition +
  6. Subtraction -
"""
print(3 * 3 + 6 / 3 - 3) # 8.0
print(3 * (3 + 3) / 3 - 3) # 3.0

