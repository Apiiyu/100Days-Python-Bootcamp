# Loops

# For Loop
# Syntax: for <variable> in <sequence>:
fruits = ["apple", "banana", "cherry"]
for fruit in fruits: # Please, use the meaningful variable name to make your code more readable.
  print(fruit)
 
total = 0 
for number in range(1, 101): # range(start, end, step)
  total += number

print(total) # 5050