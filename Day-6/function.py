# Function

def say_hello(): # Here's how you define the function
  print("Hello")
  print("How are you?") 
  
say_hello() # Here's how you call the function

# Function with input
def say_hello_with_name(name): # You also can add parameter to the function
  print(f"Hello {name}")
  print(f"How are you {name}?")

say_hello_with_name("Angela") # Here's how you call the function with arguments   